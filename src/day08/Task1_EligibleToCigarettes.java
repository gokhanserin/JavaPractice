package day08;

public class Task1_EligibleToCigarettes {

    public static void main(String[] args) {

        String name = "Sally";
        byte age = 17;

        if (age >= 18) {
            System.out.println(name + " is eligible to buy cigarettes");
        }else{
            System.out.println(name + " is NOT eligible to buy cigarettes");
        }

        /*
        1. Given a number (byte) age, write a program that can check if the
person is eligible to buy Cigarettes
         */

    }

}
